async function addOpportunities() {
  console.log('Adding new funding opportunities to Supabase...\n');

  // Get existing opportunities to avoid duplicates
  const { data: existing } = await supabase
    .from('funding_opportunities')
    .select('name');

  const existingNames = new Set((existing || []).map(o => o.name.toLowerCase()));

  let added = 0;
  let skipped = 0;

  for (const opp of newOpportunities) {
    if (existingNames.has(opp.name.toLowerCase())) {
      console.log(`  Skipped (exists): ${opp.name}`);
      skipped++;
      continue;
    }

    const { error } = await supabase
      .from('funding_opportunities')
      .insert({
        ...opp,
        created_date: new Date().toISOString(),
      });

    if (error) {
      console.error(`  Error adding ${opp.name}:`, error.message);
    } else {
      console.log(`  Added: ${opp.name}`);
      added++;
    }
  }

  console.log(`\n✅ Complete! Added ${added} new opportunities, skipped ${skipped} duplicates.`);
}

